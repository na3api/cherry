function game_life() {
    $('#stop').find('.glyphicon').removeClass('glyphicon-play');
    $('#stop').find('.glyphicon').addClass('glyphicon-pause');
    $('#next').prop('disabled', true);
    for (i = 0; i < height / box_size; i++) {
        life[i] = [];
        step[i] = [];
        for (j = 0; j < width / box_size; j++) {
            if (i > ((height / box_size - first_generation.length) / 2) &&
                    i <= ((height / box_size - first_generation.length) / 2) + first_generation.length &&
                    j > ((width / box_size - first_generation[0].length) / 2) &&
                    j <= ((width / box_size - first_generation[0].length) / 2) + first_generation[0].length)
            {
                try {
                    life[i][j] = first_generation[i - parseInt((height / box_size - first_generation.length) / 2) - 1][j - parseInt((width / box_size - first_generation[0].length) / 2) - 1 ];
                } catch (err) {
                    console.log((height / box_size - first_generation.length) / 2, (width / box_size - first_generation[0].length) / 2);
                }
            } else {
                life[i][j] = 0;
            }
            step[i][j] = 0;

        }
    }
    //console.log(first_generation, box_size)
    life_time = setInterval(function () {
        life_period();
    }, step_delay)


}
function life_period() {
    for (i = 0; i < life.length; i++) {
        for (j = 0; j < life[i].length; j++) {
            var neph = 0,
                    top = i - 1,
                    bottom = i + 1,
                    left = j - 1,
                    right = j + 1;
            if (top < 0) {
                top = life.length - 1;
            }
            if (bottom > life.length - 1) {
                bottom = 0;
            }
            if (left < 0) {
                left = life[i].length - 1;
            }
            if (right > life[i].length - 1) {
                right = 0;
            }
            //
            if (life[top][left])
                neph++;

            if (life[top][j])
                neph++;

            if (life[top][right])
                neph++;
            //on this lvl
            if (life[i][left])
                neph++;

            if (life[i][right])
                neph++;
            //on bottom
            if (life[bottom][left])
                neph++;

            if (life[bottom][j])
                neph++;

            if (life[bottom][right])
                neph++;

            /* check rules */
            if (neph <= 1 || neph > 3)
                step[i][j] = 0;
            else if (neph == 3)
                step[i][j] = 1;
            else
                step[i][j] = life[i][j];

            if (life[i][j]) {
                context.fillStyle = color_life;
            } else {
                context.fillStyle = color_die;
            }
            context.fillRect(j * box_size, i * box_size, box_size, box_size);
        }
    }
    life = clone(step);
}
function clone(obj) {
    if (obj == null || typeof (obj) != 'object')
        return obj;
    var temp = new obj.constructor();
    for (var key in obj)
        temp[key] = clone(obj[key]);
    return temp;
}
